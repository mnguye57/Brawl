package brawl.model;

import brawl.model.enums.PlayerID;

/**
 * The Base class represents a Base card in play, and tracks who it belongs to.
 * 
 * @author FBRD-Megan Arnez
 * @version 1.0
 */
public class Base
{
    /**
     * Represents the owner of a base (either player 1 or player 2).
     */
    private PlayerID owner;

    /**
     * Constructs a Base associating it with an owner.
     *
     * @param owner owner of the base (1 or 2)
     */
    public Base(PlayerID owner)
    {
        this.owner = owner;
    }

    /**
     * Accessor to owner of the Base.
     *
     * @return A number representing the owner (1 or 2)
     */
    public PlayerID getOwner()
    {
        //RETURN owner
        return owner;
    }
}